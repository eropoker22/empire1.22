import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const manifestPath = resolve("packages/game-config/src/maps/empire-streets-city-map.json");
const outputPath = resolve("page-assets/js/data/empire-city-map.generated.js");
const declarationPath = resolve("page-assets/js/data/empire-city-map.generated.d.ts");

function normalizeManifestForHash(manifest) {
  return {
    id: manifest.id,
    version: manifest.version,
    width: manifest.width,
    height: manifest.height,
    districts: [...manifest.districts]
      .sort((left, right) => left.id.localeCompare(right.id, "en", { numeric: true }))
      .map((district) => ({
        id: district.id,
        legacyId: district.legacyId ?? null,
        name: district.name,
        zone: district.zone,
        polygon: district.polygon.map((point) => ({
          x: Number(Number(point.x).toFixed(3)),
          y: Number(Number(point.y).toFixed(3))
        })),
        neighborIds: [...district.neighborIds].sort((left, right) => left.localeCompare(right, "en", { numeric: true })),
        buildingSetKey: district.buildingSetKey ?? null,
        spawnZones: [...(district.spawnZones ?? [])].sort(),
        isSpawnCandidate: Boolean(district.isSpawnCandidate),
        isDowntown: Boolean(district.isDowntown)
      }))
  };
}

function stableStringify(value) {
  if (Array.isArray(value)) {
    return `[${value.map(stableStringify).join(",")}]`;
  }
  if (value && typeof value === "object") {
    return `{${Object.keys(value).sort().map((key) => `${JSON.stringify(key)}:${stableStringify(value[key])}`).join(",")}}`;
  }
  return JSON.stringify(value);
}

function createStableMapHash(manifest) {
  const text = stableStringify(normalizeManifestForHash(manifest));
  let hash = 2166136261;
  for (let index = 0; index < text.length; index += 1) {
    hash ^= text.charCodeAt(index);
    hash = Math.imul(hash, 16777619);
  }
  return `fnv1a32:${(hash >>> 0).toString(16).padStart(8, "0")}`;
}

const manifest = JSON.parse(await readFile(manifestPath, "utf8"));
const hash = createStableMapHash(manifest);
const source = [
  "/* Generated by scripts/generate-empire-city-map-assets.mjs. Do not edit by hand. */",
  `export const empireCityMapManifest = Object.freeze(${JSON.stringify(manifest)});`,
  `export const empireCityMapManifestHash = ${JSON.stringify(hash)};`,
  "export default empireCityMapManifest;",
  ""
].join("\n");

await writeFile(outputPath, source, "utf8");
await writeFile(declarationPath, [
  "import type { EmpireCityMapManifest } from '../../../packages/game-config/src/maps/empire-city-map-types';",
  "export const empireCityMapManifest: Readonly<EmpireCityMapManifest>;",
  "export const empireCityMapManifestHash: string;",
  "export default empireCityMapManifest;",
  ""
].join("\n"), "utf8");
console.log(`Wrote ${outputPath} (${hash}).`);
